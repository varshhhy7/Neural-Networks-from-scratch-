"""# Aggregations

Aggregations summarize data across an array:  
- `np.sum()` ‚Üí total sum of elements  
- `np.mean()` ‚Üí average value  
- `np.min()`, `np.max()` ‚Üí minimum/maximum  
- `np.argmax()`, `np.argmin()` ‚Üí index of maximum/minimum  

Axis parameter:  
- `axis=0` ‚Üí operation along columns (collapse rows)  
- `axis=1` ‚Üí operation along rows (collapse columns)  

In neural networks, aggregations are used for operations like **loss calculation (mean squared error)**.

"""

